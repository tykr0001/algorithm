ll answer;
int x, y;
int n;

void Solve(void) {
    for (int r = 1; r * r <= x; r++) {
        int b = x / r - 1;
        if (min(b, y) > r)
            answer += min(b, y) - r;
    }
    cout << answer << endl;
}

void Init(void) {
    cin >> x >> y;
    answer = 0;
}

