void Solve(void) {
    cnt = 0;
    flag = true;
    for (ll i = 1; i <= n; i++) {
        cin >> inp;
        cnt += inp;
        if (cnt < i * (i - 1) / 2) {
            flag = false;
        }
    }
    if (flag) {
        cout << "YES" << endl;
    }
    else {
        cout << "NO" << endl;
    }
}

