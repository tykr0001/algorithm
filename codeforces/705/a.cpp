int t;
int n, k;
vi arr;

void Solve(void) {
    for (int i = 1; i <= n; i++) {
        if (i != k && (k % 2 ? (i > k / 2) : (i >= k / 2))) {
            arr.emplace_back(i);
        }
    }

    cout << arr.size() << endl;
    for (auto elem : arr) {
        cout << elem << " ";
    }
    cout << endl;
}

void Init(void) {
    arr.clear();
    cin >> n >> k;
}

