int N;
vi arr;
vb visited;

void DFS(int cnt) {
    if (cnt == N) {
        for (auto& elem : arr) {
            cout << elem << " ";
        }
        cout << endl;
        return;
    }
    for (int i = 1; i <= N; i++) {
        if (visited[i]) continue;
        arr.emplace_back(i);
        visited[i] = true;
        DFS(cnt + 1);
        arr.pop_back();
        visited[i] = false;
    }
}

void Solve(void) {
    DFS(0);
}

void Init(void) {
    cin >> N;
    visited.resize(N + 1, false);
}

int main(void) {
    Boost;
    Init();
    Solve();
    return 0;
}