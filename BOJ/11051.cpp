int N, K;
int answer;

void Solve(void) {
    for(int i=0; i<K; i++){
        answer *= N - i;
        answer %= 10007;
    }
    for(int i=1; i<=K; i++){
        while(answer % i){
            answer += 10007;
        }
        answer /= i;
        answer %= 10007;
    }
    cout << answer;
}

