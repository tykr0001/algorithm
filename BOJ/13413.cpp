int T;
int N;
string init;
string goal;
int W, B;

void Solve(void) {
    for (int i = 0; i < N; i++) {
        if (init[i] != goal[i]) {
            if (init[i] == 'W') {
                W++;
            }
            else {
                B++;
            }
        }
    }
    cout << min(W, B) + abs(W - B) << endl;
}

void Init(void) {
    cin >> N >> init >> goal;
    W = B = 0;
}

int main(void) {
    Boost;
    cin >> T;
    while (T--) {
        Init();
        Solve();
    }
    return 0;
}